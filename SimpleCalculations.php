class SimpleCalculations{

public $x;
public $y;

function SimpleAdd($x,$y){

return ($x+$y);

}

function SimpleMin($x,$y){

return ($x-$y);

}

function SimpleMul($x,$y){

return ($x*$y);

}

function SimpleDiv($x,$y){

return ($x/$y);

}


}